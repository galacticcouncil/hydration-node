//! Migration to reset nonces from u32::MAX to correct values for affected accounts
//!
//! This migration fixes a bug where 1,804 accounts ended up with nonce = u32::MAX (4294967295).
//! The affected accounts were identified on 2025-12-09 (1,804 accounts, updated 2025-12-10) via chain scans.
//! Each account's nonce is set to either:
//! - Their EVM transaction count (if they have an EVM address in the transaction log)
//! - 0 (if they don't have an EVM address or no transactions found)

use frame_support::{
	storage_alias,
	traits::{Get, OnRuntimeUpgrade},
	weights::Weight,
};

#[cfg(feature = "try-runtime")]
use codec::{Decode, Encode};

pub mod v1 {
	use super::*;
	use crate::AccountId;
	use sp_core::crypto::Ss58Codec;

	pub struct ResetMaxNonces<T>(sp_std::marker::PhantomData<T>);

	impl<T: frame_system::Config<AccountId = AccountId, Nonce = u32>> OnRuntimeUpgrade for ResetMaxNonces<T> {
		fn on_runtime_upgrade() -> Weight {
			log::info!(
				target: "runtime::reset_max_nonces",
				"Starting migration to reset {} accounts with max nonce",
				AFFECTED_ACCOUNTS.len()
			);

			let mut reads = 0u64;
			let mut writes = 0u64;
			let mut reset_count = 0u64;
			let mut skipped_count = 0u64;

			for (account_str, new_nonce) in AFFECTED_ACCOUNTS.iter() {
				// Decode the SS58 address to AccountId
				let account_id = match AccountId::from_ss58check(account_str) {
					Ok(acc) => acc,
					Err(_) => {
						log::warn!(
							target: "runtime::reset_max_nonces",
							"Failed to decode address: {}",
							account_str
						);
						skipped_count += 1;
						continue;
					}
				};

				// Mutate the account info atomically
				reads += 1;
				let was_reset = frame_system::Account::<T>::mutate(&account_id, |account_info| {
					// Check if nonce is u32::MAX
					if account_info.nonce == u32::MAX {
						// Set nonce to the specified value
						account_info.nonce = *new_nonce;
						true
					} else {
						false
					}
				});

				if was_reset {
					writes += 1;
					reset_count += 1;

					if reset_count % 100 == 0 {
						log::info!(
							target: "runtime::reset_max_nonces",
							"Reset {} nonces so far...",
							reset_count
						);
					}
				} else {
					skipped_count += 1;
				}
			}

			log::info!(
				target: "runtime::reset_max_nonces",
					"Migration completed: {} nonces reset, {} accounts skipped",
				reset_count,
				skipped_count
			);

			T::DbWeight::get().reads_writes(reads, writes)
		}

		#[cfg(feature = "try-runtime")]
		fn pre_upgrade() -> Result<sp_std::vec::Vec<u8>, sp_runtime::TryRuntimeError> {
			log::info!(
				target: "runtime::reset_max_nonces",
				"PRE-UPGRADE: Checking {} accounts",
				AFFECTED_ACCOUNTS.len()
			);

			let mut max_nonce_count = 0u32;

			for (account_str, _new_nonce) in AFFECTED_ACCOUNTS.iter() {
				if let Ok(account_id) = AccountId::from_ss58check(account_str) {
					let account_info = frame_system::Account::<T>::get(&account_id);
					if account_info.nonce == u32::MAX {
						max_nonce_count += 1;
					}
				}
			}

			log::info!(
				target: "runtime::reset_max_nonces",
				"PRE-UPGRADE: Found {} accounts with max nonce",
				max_nonce_count
			);

			Ok(max_nonce_count.encode())
		}

		#[cfg(feature = "try-runtime")]
		fn post_upgrade(state: sp_std::vec::Vec<u8>) -> Result<(), sp_runtime::TryRuntimeError> {
			let pre_max_nonce_count = u32::decode(&mut &state[..]).map_err(|_| "Failed to decode pre-upgrade state")?;

			log::info!(
				target: "runtime::reset_max_nonces",
				"POST-UPGRADE: Verifying migration (expected {} resets)",
				pre_max_nonce_count
			);

			let mut remaining_max_nonce = 0u32;

			for (account_str, _new_nonce) in AFFECTED_ACCOUNTS.iter() {
				if let Ok(account_id) = AccountId::from_ss58check(account_str) {
					let account_info = frame_system::Account::<T>::get(&account_id);
					if account_info.nonce == u32::MAX {
						remaining_max_nonce += 1;
						log::error!(
							target: "runtime::reset_max_nonces",
							"Account still has max nonce: {}",
							account_str
						);
					}
				}
			}

			if remaining_max_nonce > 0 {
				return Err("Migration failed: some accounts still have max nonce".into());
			}

			log::info!(
				target: "runtime::reset_max_nonces",
				"POST-UPGRADE: All nonces successfully reset!"
			);

			Ok(())
		}
	}
}
